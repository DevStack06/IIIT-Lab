import java.io.*;
import java.util.Scanner;
public class For
{
	public static void  main(String argus[])
	{
		int n;
		
		for(int i=0;i<7;i++)
		{
			for(int j=0;j<=i;j++)
			{
				
			 System.out.print(+(j+1)+" ");
			}	
			System.out.println("\n");
			
	}
	for(int i=5;i>=0;i--)
		{
			for(int j=0;j<=i;j++)
			{
				
			 System.out.print(+(j+1)+" ");
			}	
			System.out.println("\n");
			
	}
	
	}


}